def disable_test_mode():
    """Disable test mode in email configuration"""
    config_path = os.path.join(os.path.dirname(__file__), 'config', 'email_config.json')
    
    try:
        with open(config_path, 'r') as f:
            config = json.load(f)
        
        config['test_mode'] = False
        
        with open(config_path, 'w') as f:
            json.dump(config, f, indent=2)
        
        print("âœ… Test mode disabled in email configuration")
        print("ğŸ“§ Emails will now be sent to MailHog")
        return True
        
    except Exception as e:
        print(f"âŒ Failed to update configuration: {e}")
        return False

